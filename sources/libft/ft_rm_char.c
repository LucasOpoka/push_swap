void	ft_rm_char(char *s, char c)
{
	int	rd;
	int	wr;

	rd = 0;
	wr = 0;
	while (s[rd])
	{
		if (s[rd] != c)
			s[wr++] = s[rd];
		rd++;
	}
	s[wr] = 0;
}
