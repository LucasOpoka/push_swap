#include "libft.h"

void	ft_prnt_d(t_printf *stc, int nb)
{
	if (nb == -2147483648)
		ft_prnt_s(stc, "-2147483648");
	else
	{
		if (nb < 0)
		{
			ft_prnt_c(stc, '-');
			nb *= -1;
		}
		if (nb / 10 > 0)
			ft_prnt_d(stc, nb / 10);
		ft_prnt_c(stc, (nb % 10) + 48);
	}
}
