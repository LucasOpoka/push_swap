#include "libft.h"

void	ft_prnt_lx(t_printf *stc, unsigned int n)
{
	if (n / 16 > 0)
		ft_prnt_lx(stc, n / 16);
	ft_prnt_c(stc, "0123456789abcdef"[n % 16]);
}
